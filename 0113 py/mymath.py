def 삼각형_넓이(x,h):
    """
    삼각형_넓이의 Docstring
    
    :param x: 가로
    :param h: 높이
    """

    return x*h/2

def 원의_넓이(r) :
    """
    원의_넓이의 Docstring
    
    :param r: 반지름
    """

    return (r*2)*3.14

def 직육면체의_넓이(x,y,h):
    """
    직육면체의_넓이의 Docstring
    
    :param x: 가로
    :param y: 세로
    :param h: 높이
    """

    return 2*(x*h+y*h+x*y)
